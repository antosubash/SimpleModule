using System.Diagnostics;
using System.Text.Json;
using Microsoft.EntityFrameworkCore;
using Microsoft.Extensions.Logging;
using SimpleModule.BackgroundJobs.Contracts;
using SimpleModule.Core.Settings;
using SimpleModule.Datasets.Contracts;
using SimpleModule.Datasets.Contracts.Events;
using SimpleModule.Datasets.Processing;
using SimpleModule.Settings.Contracts;
using SimpleModule.Storage;
using Wolverine;

namespace SimpleModule.Datasets.Jobs;

public sealed partial class ProcessDatasetJob(
    DatasetsDbContext db,
    IStorageProvider storage,
    DatasetProcessorRegistry processors,
    IMessageBus bus,
    IBackgroundJobs jobs,
    ISettingsContracts settings,
    ILogger<ProcessDatasetJob> logger
) : IModuleJob
{
    public async Task ExecuteAsync(
        IJobExecutionContext context,
        CancellationToken cancellationToken
    )
    {
        var payload = context.GetData<ProcessDatasetJobData>();
        var datasetId = DatasetId.From(payload.DatasetId);
        var row = await db.Datasets.FirstOrDefaultAsync(d => d.Id == datasetId, cancellationToken);
        if (row is null)
        {
            LogDatasetMissing(logger, payload.DatasetId);
            return;
        }

        var sw = Stopwatch.StartNew();
        row.Status = DatasetStatus.Processing;
        row.UpdatedAt = DateTimeOffset.UtcNow;
        await db.SaveChangesAsync(cancellationToken);

        DatasetStatus finalStatus;
        try
        {
            await using var stream =
                await storage.GetAsync(row.StoragePath, cancellationToken)
                ?? throw new InvalidOperationException(
                    $"Original blob missing at {row.StoragePath}"
                );

            var processor = processors.Resolve(row.Format);
            var result = await processor.ProcessAsync(stream, cancellationToken);
            result.Metadata.Common.OriginalFileName = row.OriginalFileName;
            result.Metadata.Common.SizeBytes = row.SizeBytes;
            result.Metadata.Common.ContentHash = row.ContentHash;
            result.Metadata.Common.ProcessingDurationMs = sw.Elapsed.TotalMilliseconds;

            row.SourceSrid = result.SourceSrid;
            row.Srid = result.TargetSrid;
            row.BboxMinX = result.BoundingBox?.MinX;
            row.BboxMinY = result.BoundingBox?.MinY;
            row.BboxMaxX = result.BoundingBox?.MaxX;
            row.BboxMaxY = result.BoundingBox?.MaxY;
            row.FeatureCount = result.FeatureCount;

            if (result.NormalizedGeoJson is not null)
            {
                var normalizedPath = StoragePathHelper.Combine(
                    Path.GetDirectoryName(row.StoragePath)?.Replace('\\', '/') ?? "datasets",
                    "normalized.geojson"
                );
                await using var normalizedStream = new MemoryStream(result.NormalizedGeoJson);
                var normalized = await storage.SaveAsync(
                    normalizedPath,
                    normalizedStream,
                    "application/geo+json",
                    cancellationToken
                );
                row.NormalizedPath = normalized.Path;
            }

            row.MetadataJson = JsonSerializer.Serialize(result.Metadata);
            row.Status = DatasetStatus.Ready;
            row.ErrorMessage = null;
            row.ProcessedAt = DateTimeOffset.UtcNow;
            row.UpdatedAt = DateTimeOffset.UtcNow;
            finalStatus = DatasetStatus.Ready;
            await db.SaveChangesAsync(cancellationToken);
            LogDatasetProcessed(logger, payload.DatasetId, row.Format);

            if (
                await settings.GetSettingAsync<bool?>(
                    DatasetsConstants.SettingKeys.AutoGenerateDefaultDerivative,
                    SettingScope.Application
                )
                is true
                    or null
            )
            {
                var target = row.Format.IsRaster() ? DatasetFormat.Cog : DatasetFormat.PmTiles;
                await jobs.EnqueueAsync<ConvertDatasetJob>(
                    new ConvertDatasetJobData
                    {
                        DatasetId = payload.DatasetId,
                        TargetFormat = (int)target,
                    },
                    cancellationToken
                );
            }
        }
#pragma warning disable CA1031 // Top-level job handler surfaces failures through the row.
        catch (Exception ex)
#pragma warning restore CA1031
        {
            row.Status = DatasetStatus.Failed;
            row.ErrorMessage = ex.Message;
            row.ProcessedAt = DateTimeOffset.UtcNow;
            row.UpdatedAt = DateTimeOffset.UtcNow;
            finalStatus = DatasetStatus.Failed;
            await db.SaveChangesAsync(CancellationToken.None);
            LogDatasetFailed(logger, payload.DatasetId, ex);
        }

        await bus.PublishAsync(new DatasetProcessed(datasetId, finalStatus));
    }

    [LoggerMessage(Level = LogLevel.Warning, Message = "Dataset {Id} not found for processing")]
    private static partial void LogDatasetMissing(ILogger logger, Guid id);

    [LoggerMessage(Level = LogLevel.Information, Message = "Dataset {Id} processed ({Format})")]
    private static partial void LogDatasetProcessed(ILogger logger, Guid id, DatasetFormat format);

    [LoggerMessage(Level = LogLevel.Error, Message = "Dataset {Id} processing failed")]
    private static partial void LogDatasetFailed(ILogger logger, Guid id, Exception exception);
}

public sealed class ProcessDatasetJobData
{
    public Guid DatasetId { get; set; }
}
